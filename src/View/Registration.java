    private void jTextField9KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField9KeyReleased
            String name = jTextField9.getText();
        
        try {
          DefaultTableModel dt =  (DefaultTableModel) jTable1.getModel();
         dt.setRowCount(0);
         Statement s = DBConnection.getStatementConnection();
         ResultSet rs = s.executeQuery("SELECT * FROM registration WHERE name = '"+name+"' ");
          while (rs.next()) {              
              
              Vector v =new Vector();
              v.add(rs.getString(1));
              v.add(rs.getString(2));
              v.add(rs.getString(3)); 
              dt.addRow(v);
          }   
      } catch (Exception e) {
      } // TODO add your handling code here:
    }//GEN-LAST:event_jTextField9KeyReleased

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        JOptionPane.showMessageDialog(this, "You should enter password field to edit your registration details (You Cannot edit email address) ");
        
        String name = jTable1.getValueAt(i, 0).toString();
        String email = jTable1.getValueAt(i, 1).toString();
        String number = jTable1.getValueAt(i, 2).toString();
        
       fname.setText(name);
       jTextField3.setText(email);
       jTextField6.setText(number);
       btnCreate.setEnabled(false);
    }//GEN-LAST:event_jTable1MouseClicked

